module multiplexer_8to1_4bit (
    input [3:0] D0, D1, D2, D3, D4, D5, D6, D7, // 8 входів по 4 біти
    input A2, A1, A0,                          // 3 адресні лінії
    output [3:0] Q                             // 4-бітний вихід
);

    // Реалізація через логічні маски (згідно з вашим прикладом)
    assign Q = ({4{~A2 & ~A1 & ~A0}} & D0) |
               ({4{~A2 & ~A1 &  A0}} & D1) |
               ({4{~A2 &  A1 & ~A0}} & D2) |
               ({4{~A2 &  A1 &  A0}} & D3) |
               ({4{ A2 & ~A1 & ~A0}} & D4) |
               ({4{ A2 & ~A1 &  A0}} & D5) |
               ({4{ A2 &  A1 & ~A0}} & D6) |
               ({4{ A2 &  A1 &  A0}} & D7);

endmodule
